
import java.util.Arrays;
import java.util.List;

/*Write a program that filters an array of strings, leaving only 
 the strings with length greater than 3 characters. Use .filter()*/
public class Problem1FilterArray {

    public static void main(String[] args) {
        List<String> test1=Arrays.asList("Gosho","gos","pesho","pes","blabla", "bla");
        List<String> test2=Arrays.asList("Kogato","cyfnat","nalymite");
        List<String> test3=Arrays.asList("To","be","or","not","to","be.");
        test1.stream().filter(p -> p.length()>3).forEach(System.out::println);
        test2.stream().filter(p -> p.length()>3).forEach(System.out::println);
        test3.stream().filter(p -> p.length()>3).forEach(System.out::println);

    }

    
}
